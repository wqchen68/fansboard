		Glossary<br />
FGA: Field Goals Attempted	FGM: Field Goals Made	FG%: Field Goal Percentage<br />
FTA: Free Throws Attempted	FTM: Free Throws Made	FT%: Free Throw Percentage<br />
3PTA: 3-point Shots Attempted	3PTM: 3-point Shots Made	3PT%: 3-point Percentage<br />
OREB: Offensive Rebounds	DREB: Defensive Rebounds	REB: Total Rebounds<br />
PTS: Points Scored	AST: Assists	ST: Steals<br />
BLK: Blocked Shots	A/T: Assist/Turnover Ratio	TO: Turnover<br />
<br />
In addition, based on Fantasy Game considerations, we adjusted the proportional categories, such as FG%, FT%, 3PT%,A/T. Way of adjustment is based on the weighted NUMBER of Shots. A player FG% is high but the shot rarely, there is no impact on your team. Conversely, a player FG% is high and much more shots, there is more impact on your team. So we adjusted as follows:
<br />
WFG%: Weighted Field Goal Percentage by Field Goal Attempted<br />
WFT% : Weighted  Free Throw Percentage by Free Throw Attempted<br />
W3PT% : Weighted 3-point Percentage by 3-point Shots Attempted<br />
WA/T : Weighted Assist/Turnover Ratio by Assist<br />
<br />
Therefore, under the adjustment, Kevin Durant (OKC-SF) free throws will give the team more help. Conversely, Dwight  Howard (Hou-C) free throws will give the team more impact.<br /><br /><br />
		
		
